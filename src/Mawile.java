public class Mawile extends Pokemon{
    public Mawile(String weight, String height, int hp, int atk, int def, int spA, int spD, int spe, int totBS, String type){
        this.name = "MAWILE";
        this.weight = weight;
        this.height = height;
        this.hp = hp;
        this.atk = atk;
        this.def = def;
        this.spA = spA;
        this.spD = spD;
        this.spe = spe;
        this.totBS = totBS;
        this.type = type;
    }
}
