public abstract class Pokemon {

    protected String name;
    protected  String type;

    protected int atk;
    protected int def;
    protected int spd;

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }

    public int getAtk() {
        return atk;
    }

    public int getDef() {
        return def;
    }

    public int getSpd() {
        return spd;
    }
}
